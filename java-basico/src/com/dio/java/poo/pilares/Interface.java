/**
 * Uma interface em Java serve para garantir que as classes que a implementam
 * forneçam implementações específicas dos métodos definidos na interface.
 * Interfaces são usadas para definir contratos de comportamento que devem
 * ser seguidos pelas classes que a implementam.
 */
public interface Interface {
    // Defina métodos abstratos que as classes que implementam esta interface
    // deverão obrigatoriamente implementar.

    public void atualizar();
    public void copiar();
    public void deletar();

}
